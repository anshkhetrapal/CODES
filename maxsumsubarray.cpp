class Solution{   
public:
    long maximumSumSubarray(int K, vector<int> &Arr , int N){
        long wstart=0,maxi=0;
        long  wend=0,sum=0;
        while(wend<N){
             sum+=Arr[wend];
            if(wend-wstart+1==K){
                maxi=max(maxi,sum);
                sum-=Arr[wstart];
                wstart++;
                
            }
            wend++;
            
        }
        return maxi;
    }
};

